/// A link icon that can be clicked.
#[component]
pub fn LinkIcon(link: Link) -> impl IntoView {
    let class = "inline-block \
        text-center uppercase relative \
        leading-snug group";

    let hover_class = "hidden \
        group-hover:block rounded text-xs \
        absolute z-10 width-fit p-1.5 \
        text-neutral-200 font-bold \
        bg-neutral-800 text-center \
        top-[110%] right-0 whitespace-pre";

    view! {
        <a class=class href={link.href()} target="_blank" rel="noopener noreferrer">
            <>
                {link.icon()}
                <span class=hover_class>
                    {link.text()}
                </span>
            </>
        </a>
    }
}
